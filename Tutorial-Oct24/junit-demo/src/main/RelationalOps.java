package main;

public class RelationalOps {

	///This function checks if two numbers are equal
	public boolean isEqual(int num1, int num2) {
		return num1 == num2;
	}

	public boolean isNotEqual(int num1, int num2) {
		return num1 != num2;
	}

	/**
	 * 
	 * @param num1
	 * @param num2
	 * @return
	 * This function checks if num1 is less than num2
	 */
	public boolean isLess(int num1, int num2) {
		return num1 < num2;
	}

	public boolean isGreater(int num1, int num2) {
		return num1 > num2;
	}

}
